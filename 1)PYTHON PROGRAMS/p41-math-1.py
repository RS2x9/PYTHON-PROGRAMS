import math
#math.sqrt(num)
print(math.sqrt(4))
a=math.sqrt(9)
print(int(a))

#math.ceil(num)  returns smallest not less than num
print(math.ceil(1.5))
print(math.ceil(2))
print(math.ceil(-1.5))
print()
#math.exp(num) returns e raised to num power
print(math.exp(1))
print(math.exp(2))
print()
#math.fabs(num)  returns the absolute(modulas)of a number
print(math.fabs(1.5))
print(math.fabs(1))
print()
#math.floor(num)  returns the largest integer
#not greater than num
print(math.floor(1.4))

#math.log(num,base)
print(math.log(1024,2))
print(math.log(9,3))

#math.log<any positive integer>(num)
print(int(math.log10(1)))
print(int(math.log10(10)))
print(int(math.log2(4)))

#math.pow(base,pow)
print(math.pow(2,3))

#math.sin(angle) angle in radian only
print(math.sin(0.86)) #prints value of sin(60)
print()
#math.cos(angle in radians)
print(math.cos(0.86))
print()
#math.tan(angle in radians)
print(math.tan(0.86))
print()
#math.degrees(angle to be converted from radians to degree)
print(math.degrees(22/7))
print(math.degrees(1.0476))
print()
#math.radians(degree to radian)
print(math.radians(60))
print()
# math.pi   and math.e
print(math.pi)
print(math.e)

